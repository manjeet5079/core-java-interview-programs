package com.demo.string;

public class StringSwitchCaseDemo {

	public static void main(String[] args) {

		String s = "A";

		switch (s) {
		case "a":
			System.out.println("Simple A");
			break;
		default:
			System.out.println("Default");
			break;
		case "B":
			System.out.println("Capital A");
			break;
		
		}
	}
}
